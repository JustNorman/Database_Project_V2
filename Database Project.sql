CREATE TABLE [EMPLOYEE] (
  [EMP_ID] INT,
  [EMP_FNAME] VARCHAR(50),
  [EMP_LNAME] VARCHAR(50),
  [EMP_ADDRESS] VARCHAR(100),
  [EMP_PHONE] VARCHAR(15),
  [EMP_DOB] DATE,
  [EMP_TITLE] VARCHAR(50),
  [TYPE] VARCHAR(20),
  [MANAGER_ID] INT,
  PRIMARY KEY ([EMP_ID]),
  CONSTRAINT [FK_EMPLOYEE.EMP_ID]
    FOREIGN KEY ([EMP_ID])
      REFERENCES [EMPLOYEE]([EMP_LNAME])
);